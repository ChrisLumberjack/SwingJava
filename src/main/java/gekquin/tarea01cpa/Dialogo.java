    public class DateDocumentFilter extends DocumentFilter {

        private static final String DATE_PATTERN = "\\d{0,2}(\\d{0,2})?(/\\d{0,2})?(/\\d{0,4})?";

        @Override
        public void insertString(FilterBypass fb, int offset, String string, AttributeSet attr) throws BadLocationException {
            if (string == null) {
                return;
            }
            String newText = getUpdatedText(fb, offset, string);
            if (isValidDate(newText)) {
                super.insertString(fb, offset, string, attr);
            }
        }

        @Override
        public void replace(FilterBypass fb, int offset, int length, String text, AttributeSet attrs) throws BadLocationException {
            if (text == null) {
                return;
            }
            String newText = getUpdatedText(fb, offset, text);
            if (isValidDate(newText)) {
                super.replace(fb, offset, length, text, attrs);
            }
        }

        @Override
        public void remove(FilterBypass fb, int offset, int length) throws BadLocationException {
            String newText = getUpdatedText(fb, offset, "");
            if (isValidDate(newText)) {
                super.remove(fb, offset, length);
            }
        }

        private String getUpdatedText(FilterBypass fb, int offset, String string) throws BadLocationException {
            Document doc = fb.getDocument();
            StringBuilder sb = new StringBuilder(doc.getText(0, doc.getLength()));
            sb.insert(offset, string);
            return sb.toString();
        }

        private boolean isValidDate(String text) {
            return Pattern.matches(DATE_PATTERN, text);
        }
    }

