    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        JComboBox<String> comboBox = new JComboBox<>(new String[]{"Trail", "Cross country", "Descenso", "Fat bike", "Carretera"});
        DefaultComboBoxModel<String> model = (DefaultComboBoxModel<String>) comboBox.getModel();

        String selectedType = (String) jComboBox1.getSelectedItem();

        switch (selectedType) {
            case "Trail":
                jComboBox2.setModel(new DefaultComboBoxModel<>(new String[]{"26", "27.5", "29"}));
                jComboBox3.setModel(new DefaultComboBoxModel<>(new String[]{"Aluminio", "Carbono"}));
                jComboBox2.setVisible(true);
                jComboBox3.setVisible(true);
                break;
            case "Carretera":
                jComboBox2.setModel(new DefaultComboBoxModel<>(new String[]{"Aluminio", "Carbono"}));
                jComboBox3.setModel(new DefaultComboBoxModel<>(new String[]{"Frenos de disco", "Pastillas de freno"}));
                jComboBox2.setVisible(true);
                jComboBox3.setVisible(true);
                break;
            default:
                jComboBox2.setVisible(false);
                jComboBox3.setVisible(false);
                break;
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

